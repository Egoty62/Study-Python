    # 따라서 직접 실행하는 파일에는 상대참조를 하면 안 되고, 종속되는 파일에만 상대참조를 하는 것이 좋음
        # 또한 패키지 전체를 보는 입장에서 모든 패키지의 경로를 정확히 기록하는 것이 이해가 쉬움
            # 따라서 가능하면 패키지 내에서 모듈을 정의할 때는 절대 참조가 좋음